static int readable_file(const char *path) {
  FILE *file;

  if (!path || !path[0]) {
    return 0;
  }
  file = fopen(path, "rb");
  if (!file) {
    return 0;
  }
  fclose(file);
  return 1;
}

static const char *find_default_opl4_rom(const KSPResources *resources) {
  static const char *const environment_names[] = {
      "KSP_YRW801_ROM", "YRW801_ROM"};
  static const char *const local_names[] = {"yrw801.rom", "YRW801.ROM"};
  const char *path;
  size_t i;

  /* A plain KSS still behaves as before: enable MoonSound explicitly with
   * --opl4-rom. KSP containers generated by the MoonSound tools need it by
   * default, however, or their OPL4 channels are silently absent. */
  if (!resources || !resources->is_ksp) {
    return NULL;
  }

  for (i = 0; i < sizeof(environment_names) / sizeof(environment_names[0]);
       i++) {
    path = getenv(environment_names[i]);
    if (readable_file(path)) {
      return path;
    }
  }

  for (i = 0; i < sizeof(local_names) / sizeof(local_names[0]); i++) {
    if (readable_file(local_names[i])) {
      return local_names[i];
    }
  }

  if (readable_file(KSP_DEFAULT_YRW801_ROM)) {
    return KSP_DEFAULT_YRW801_ROM;
  }
  return NULL;
}
